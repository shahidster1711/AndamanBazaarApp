
export interface Profile {
  id: string;
  phone_number?: string;
  name?: string;
  email?: string;
  profile_photo_url?: string;
  city?: string;
  area?: string;
  is_location_verified: boolean;
  total_listings: number;
  successful_sales: number;
  trust_level: 'newbie' | 'verified' | 'legend';
  created_at: string;
}

export interface Category {
  id: string;
  name: string;
  slug: string;
  icon_url?: string;
  description?: string;
}

export interface Subcategory {
  id: string;
  category_id: string;
  name: string;
  slug: string;
}

export type ItemCondition = 'new' | 'like_new' | 'good' | 'fair';
export type ItemAge = '<1m' | '1-6m' | '6-12m' | '1-2y' | '2-5y' | '5y+';
export type ListingStatus = 'draft' | 'pending_review' | 'active' | 'sold' | 'expired' | 'deleted';
export type ModerationStatus = 'auto_approved' | 'pending_review' | 'approved' | 'rejected';

export interface ContactPreferences {
  chat: boolean;
  phone?: boolean;
  whatsapp?: boolean;
}

export interface AiMetadata {
  suggested_title?: string;
  suggested_description?: string;
  suggested_category?: string;
  suggested_condition?: ItemCondition;
  estimated_price_range?: { low: number; high: number };
  title_accepted?: boolean;
  description_accepted?: boolean;
  price_accepted?: boolean;
}

export interface Listing {
  id: string;
  user_id: string;
  category_id: string;
  subcategory_id?: string;
  title: string;
  description: string;
  price: number;
  condition: ItemCondition;
  city: string;
  area?: string;

  // Pricing
  is_negotiable: boolean;
  min_price?: number;

  // Item details
  item_age?: ItemAge;
  has_warranty: boolean;
  warranty_expiry?: string;
  has_invoice: boolean;
  accessories: string[];

  // Contact
  contact_preferences: ContactPreferences;

  // Status & moderation
  status: ListingStatus;
  moderation_status: ModerationStatus;
  moderation_notes?: string;
  is_featured: boolean;
  views_count: number;

  // Draft
  draft_step?: number;
  idempotency_key?: string;

  // AI
  ai_metadata?: AiMetadata;

  // Relations
  created_at: string;
  updated_at?: string;
  images?: ListingImage[];
  seller?: Profile;
}

export interface ListingImage {
  id: string;
  listing_id: string;
  image_url: string;
  display_order: number;
}

export interface Favorite {
  id: string;
  user_id: string;
  listing_id: string;
  created_at: string;
  listing?: Listing;
}

export interface Chat {
  id: string;
  listing_id: string;
  buyer_id: string;
  seller_id: string;
  last_message?: string;
  last_message_at: string;
  buyer_unread_count: number;
  seller_unread_count: number;
  listing?: Listing;
  other_party?: Profile;
  // Fix: Added buyer and seller properties to satisfy Supabase aliased joins
  buyer?: Profile;
  seller?: Profile;
}

export interface Message {
  id: string;
  chat_id: string;
  sender_id: string;
  message_text: string;
  image_url?: string;
  is_read: boolean;
  created_at: string;
}

// ===== Draft Types =====

export interface DraftListing {
  step: number;
  category?: string;
  subcategory?: string;
  title: string;
  description: string;
  price: string;
  condition: ItemCondition;
  is_negotiable: boolean;
  min_price?: string;
  item_age?: ItemAge;
  has_warranty: boolean;
  warranty_expiry?: string;
  has_invoice: boolean;
  accessories: string[];
  city: string;
  area: string;
  contact_preferences: ContactPreferences;
  image_previews: string[];
  idempotency_key: string;
  updated_at: number;
}

// ===== Price Suggestion Types =====

export interface PriceSuggestion {
  avg_price: number;
  min_price: number;
  max_price: number;
  listing_count: number;
}

// ===== AI Suggestion Types =====

export interface AiSuggestion {
  suggested_title?: string;
  suggested_description?: string;
  suggested_category?: string;
  suggested_condition?: ItemCondition;
  estimated_price_range?: { low: number; high: number };
}